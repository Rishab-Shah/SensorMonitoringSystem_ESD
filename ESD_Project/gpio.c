/*******************************************************************************
* @Function switch_init
* @Description: init switch for input pullup (P1.1 and P1.4)
* @input param : None
* @return: None
*******************************************************************************/
void gpio_init()
{
    //Configure GPIO
    P1->DIR |= BIT0;
    P1->OUT &= ~BIT0;

    P4->DIR |= BIT0;
    P4->OUT &= ~BIT0;
}
